#!/usr/bin/env python3
"""
generate_demiurge_offline.py
Self-contained — all citations are embedded in source code. No network calls.
Generates assets/texts/demiurge/{sector}.json with >= 200 entries each.

Usage:
    python3 tools/generate_demiurge_offline.py

Each sector entry format:
    { "opening": str, "citation": str, "author": str, "closing": str }

Generation strategy:
    Each sector defines raw_quotes (citation, author pairs), themed openings,
    and themed closings. Entries are generated by systematic pairing across
    multiple passes until target >= 200 is reached. Each (quote, opening,
    closing) triple is unique per entry.
"""

import json
import sys
from pathlib import Path

SCRIPT_DIR = Path(__file__).parent
OUTPUT_DIR = SCRIPT_DIR.parent / "assets" / "texts" / "demiurge"
TARGET = 200


# ---------------------------------------------------------------------------
def generate_entries(raw_quotes, openings, closings, target=TARGET):
    """Generate >= target unique (opening, citation, author, closing) dicts."""
    entries = []
    used = set()  # (quote_idx, opening_idx, closing_idx)
    n_o = len(openings)
    n_c = len(closings)

    pass_num = 0
    max_passes = n_o * n_c  # upper bound — ensures termination
    while len(entries) < target and pass_num < max_passes:
        for q_idx, (citation, author) in enumerate(raw_quotes):
            if len(entries) >= target:
                break
            o_idx = (q_idx * 3 + pass_num * 7) % n_o
            c_idx = (q_idx * 5 + pass_num * 11 + 3) % n_c
            key = (q_idx, o_idx, c_idx)
            if key not in used:
                used.add(key)
                entries.append({
                    "opening": openings[o_idx],
                    "citation": citation,
                    "author": author,
                    "closing": closings[c_idx],
                })
        pass_num += 1

    return entries


