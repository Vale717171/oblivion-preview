def main():
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    sectors = {
        "giardino":     (GIARDINO_QUOTES,    GIARDINO_OPENINGS,    GIARDINO_CLOSINGS),
        "osservatorio": (OSSERVATORIO_QUOTES, OSSERVATORIO_OPENINGS, OSSERVATORIO_CLOSINGS),
        "galleria":     (GALLERIA_QUOTES,     GALLERIA_OPENINGS,    GALLERIA_CLOSINGS),
        "laboratorio":  (LABORATORIO_QUOTES,  LABORATORIO_OPENINGS, LABORATORIO_CLOSINGS),
        "universale":   (UNIVERSALE_QUOTES,   UNIVERSALE_OPENINGS,  UNIVERSALE_CLOSINGS),
    }

    all_ok = True
    for sector, (raw_quotes, openings, closings) in sectors.items():
        entries = generate_entries(raw_quotes, openings, closings, TARGET)
        count = len(entries)
        if count < TARGET:
            print(f"  WARNING: {sector} has only {count} entries (< {TARGET})", file=sys.stderr)
            all_ok = False

        data = {"sector": sector, "responses": entries}
        out_path = OUTPUT_DIR / f"{sector}.json"
        with open(out_path, "w", encoding="utf-8") as fh:
            json.dump(data, fh, ensure_ascii=False, indent=2)

        print(f"  {sector}.json — {count} entries → {out_path}")

    if all_ok:
        print(f"\nAll sectors: >= {TARGET} entries. Done.")
    else:
        print(f"\nSome sectors did not reach {TARGET} entries. See warnings above.", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()
